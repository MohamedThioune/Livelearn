                    <p class="title-artikel">The Importance Of Intrinsic Motivation for Students</p>
                    <div class="block-content-text-artikel">
                        <p>Let’s start with the obvious line of answers. “Everyone goes to school,” or “everyone needs an education in this day and age.” Though there is a certain amount of truth to these cliched responses. But if one of these “‘cuz everyone does it” answers is the best reason a young person can present for attending school, it’s no wonder these same kids do not engage and simply do school because school is the done thing.</p>
                        <p>Senior students often invoke the mantra “I have to do well at school in order to get into university” when asked about reasons for attending and doing well. This can be a genuine motivational factor, especially in families where the expectation is academic and career success. But is this the answer we really want when we pose that question? Doing well simply to get to the next level is fine when playing video games, but it hardly seems inspirational as an educational goal for a secondary school student.</p>
                        <p>Prevalent assessment practices are the most obvious example of how we rely too heavily on extrinsic motivators. As long as we evaluate more than we assess–and as long as we provide grades more often than we provide feedback–student motivation will come from the collection of this ‘currency’ that we call marks. Those richest in this currency will be afforded the best opportunities come to the end of high school, an unfortunate fact. The students we label ‘mark sharks’ are simply the ones who have truly taken
                            to heart our message that good grades (i.e. extrinsic rewards), as opposed to quality learning, are the primary goal of our educational systems.</p>
